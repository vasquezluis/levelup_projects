console.log(typeof false);

// no primitivos
console.log(typeof {});
console.log(typeof []);

console.log(typeof new Date());
console.log(typeof new RegExp());

console.log(typeof new Number(100));
console.log(typeof new String("Hola"));
console.log(typeof new Boolean(false));
